
public class Pitch {
	
	final int LENGTH = 244;
	final int WIDTH = 122;
	
	final int GLENGTH = 60;	
	final int GPOS = (WIDTH-GLENGTH)/2;
	
	public int getLENGTH() {
		return LENGTH;
	}
	public int getWIDTH() {
		return WIDTH;
	}
	public int getGLENGTH() {
		return GLENGTH;
	}
	public int getGPOS() {
		return GPOS;
	}
		
}
